#include <bits/stdc++.h>
using namespace std;

int main()
{
    int t;
    cin >> t;

    while (t--)
    {
        int n;
        cin >> n;

        vector<int> a(n);
        for (int i = 0; i < n; i++)
        {
            cin >> a[i];
        }

        // Use a priority queue to efficiently get the two largest elements
        priority_queue<int> pq(a.begin(), a.end());

        while (pq.size() > 1)
        {
            int max1 = pq.top();
            pq.pop();
            int max2 = pq.top();
            pq.pop();

            // Choose x as close to max1+max2 as possible to maximize
            // while still forming a valid triangle
            int x = max1 + max2 - 1;
            pq.push(x);
        }

        cout << pq.top() << endl;
    }

    return 0;
}

