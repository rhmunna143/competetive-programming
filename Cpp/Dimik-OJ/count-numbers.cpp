#include <bits/stdc++.h>
using namespace std;

int main()
{
    int T;
    cin >> T;
    cin.ignore(); // To ignore the newline character after reading T

    while (T--)
    {
        string numbers;
        getline(cin, numbers);

        int count = 0;
        stringstream ss(numbers);
        string temp;

        while (ss >> temp) {
            count++;
        }

        if (T > 0) cout << count << endl;
        else cout << count;
    }

    return 0;
}

