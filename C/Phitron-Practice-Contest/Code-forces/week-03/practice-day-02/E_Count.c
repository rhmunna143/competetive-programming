#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

int main()
{
    /* Enter your code here. Read input from STDIN. Print output to STDOUT */
    char S[1000001]; 
    scanf("%s", S);

    // length of string
    int len = strlen(S);

    // sum of digits of the string
    long long int sum = 0;

    for (int i = 0; i < len; i++)
    {
        sum += S[i] - '0'; // Convert character to integer and add to sum
    }

    printf("%lld\n", sum);

    return 0;
}

